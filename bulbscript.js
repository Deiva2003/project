function on(){
    document.getElementById("bulb").style.backgroundColor= "yellow";
    document.getElementById("bulb").style.boxShadow= "10px 0px 100px 20px yellow";
}
function off(){
    document.getElementById("bulb").style.backgroundColor="rgb(240, 236, 236)";
    document.getElementById("bulb").style.boxShadow= "none";
}